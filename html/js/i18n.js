const STORAGE_KEY = "wow.lang";
const SUPPORTED = ["es", "en"];

function detectLang() {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored && SUPPORTED.includes(stored)) return stored;
  const browser = (navigator.language || navigator.userLanguage || "en").toLowerCase();
  return browser.startsWith("es") ? "es" : "en";
}

function applyTranslations(lang) {
  const dict = translations[lang] || translations.en;

  document.documentElement.lang = lang;
  if (dict["doc.title"]) document.title = dict["doc.title"];
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && dict["doc.description"]) metaDesc.setAttribute("content", dict["doc.description"]);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] != null) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key] != null) el.innerHTML = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key] != null) el.placeholder = dict[key];
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
    const key = el.getAttribute("data-i18n-alt");
    if (dict[key] != null) el.alt = dict[key];
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
    const key = el.getAttribute("data-i18n-aria-label");
    if (dict[key] != null) el.setAttribute("aria-label", dict[key]);
  });

  document.querySelectorAll(".lang-switch__btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang ? "true" : "false");
  });
}

function setLang(lang) {
  if (!SUPPORTED.includes(lang)) lang = "en";
  localStorage.setItem(STORAGE_KEY, lang);
  applyTranslations(lang);
}

document.addEventListener("DOMContentLoaded", () => {
  applyTranslations(detectLang());

  document.querySelectorAll(".lang-switch__btn").forEach((btn) => {
    btn.addEventListener("click", () => setLang(btn.dataset.lang));
  });

  const form = document.querySelector(".register__form");
  if (form) {
    form.addEventListener("submit", (e) => {
      e.preventDefault();
      const lang = document.documentElement.lang || "en";
      const dict = translations[lang] || translations.en;
      alert(dict["register.alert"]);
    });
  }
});

window.WOW_I18N = { setLang, applyTranslations, translations };
